// #region For loop from 1 to 100
for (let i = 1; i <= 100; i++) {

    // Resulting string to print
    let finalString = '';

    // IF number is a multiple of 3
    if (i % 3 == 0) {

        // Add 'Fizz' to finalString
        finalString += 'Fizz';

    }

}
// #endregion For loop from 1 to 100